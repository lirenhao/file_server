    IS '文件上传日期';
COMMENT ON COLUMN T_B_FILE_INFO.IN_TIME
    IS '文件上传时间';
COMMENT ON COLUMN T_B_FILE_INFO.FILENAME
    IS '文件名称';
COMMENT ON COLUMN T_B_FILE_INFO.FILEDATE
    IS '文件日期';
